// Constant(cons) use for those function which value are not changeable
const countryName = "Bangladesh";
console.log(countryName);

const name1 = 'Javed';
console.log(name1.length);

// Let use for value changeable  functions
let age = 18;
age = 19;
age = 20;
console.log(age);

// Conditions
let name2 = 'Akber';
if (name2.length > 8) {
    name2 = 'Akber Shamim';
}
console.log(name2);

// Replace
let name3 = 'Akber Javed';
if (name3.length > 10) {
    name3 = 'Javed Akber';
}
console.log(name3);

